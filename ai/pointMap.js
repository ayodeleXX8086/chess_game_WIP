export function pieceMap(piece, computer_player) {
  let pieceMap = [];
  if (piece.code === PieceType.PAWN) {
    pieceMap = mapPoints[0];
  } else if (piece.code === PieceType.BISHOP) {
    pieceMap = mapPoints[1];
  } else if (piece.code === PieceType.KNIGHT) {
    pieceMap = mapPoints[2];
  } else if (piece.code === PieceType.ROOK) {
    pieceMap = mapPoints[3];
  } else if (piece.code === PieceType.QUEEN) {
    pieceMap = mapPoints[4];
  } else if (piece.code === PieceType.KING) {
    pieceMap = mapPoints[5];
  }

  // check color of piece
  if (piece.color === computer_player) {
    return pieceMap;
  } else {
    return pieceMap.map((row) => row.map((value) => value * -1));
  }
}
